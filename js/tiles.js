function mapTiles() {
  const mapSize = 40;
  const mapLength = 40;

  // const mapSize = 40;
  // const mapLength = 60;

  const tileEmpty = 0;
  const tilePlatform = 1;
  const tileGround = 2;
  const tileObject = 3;
  const tileMainCharacter = 4;
  const tileOpponent = 5;

  stroke(0, 0, 0, 10);

  for (let y = 0; y < mapLength; y++) {
    for (let x = 0; x < mapLength; x++) {
      stroke(0, 0, 0);
      fill(255, 255, 255);
      if (x % 2 === 0) {
        if (y % 2 === 0) {
          // if x / y is even number - color black
          fill(0, 0, 0, 20);
        } else {
          // if x / y is uneven number - color white
          fill(255, 255, 255, 20);
        }
      } else {
        if (y % 2 === 0) {
          fill(255, 255, 255, 20);
        } else {
          fill(0, 0, 0, 20);
        }
      }
      rect(x * mapSize, y * mapSize, mapSize, mapSize);
    }
  }
}

